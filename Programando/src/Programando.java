

public class Programando {
	public static void main(String[]args) {
		int A= 101;
		System.out.println(A++);
		System.out.println(A);
		System.out.println(A+1);
		System.out.println(A++);
		System.out.println(A);
		System.out.println(--A);
		System.out.println(--A);
		System.out.println(A);
		System.out.println(A--);
		System.out.println(A++);
		
		if(A >100) {
			System.out.println("É Verdadeiro");
		
		} else
			System.out.println("É Falso");
		
		System.out.println();
		
		for (int i = -1; i < 100; i++) {
			System.out.println(i);
			
		}
			
	}
	

}
