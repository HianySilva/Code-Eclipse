
public class SalaDeAula {
	public static void main(String[] args){
		int a = 10;
		System.out.print(a++);
		System.out.print(++a);
		System.out.print(a);
		System.out.print(++a);
		System.out.print(a);
		System.out.print(a--);
		System.out.print(a);
		System.out.print(--a);
		System.out.print(a++);
		System.out.print(a);
		System.out.print(--a);
		System.out.print(a + 5);
		System.out.print(--a);
		System.out.print(a);

}
	

}
